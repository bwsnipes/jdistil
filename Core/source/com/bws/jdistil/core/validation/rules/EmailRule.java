package com.bws.jdistil.core.validation.rules;

/**
  Class used to validate email fields.
  @author - Bryan Snipes
*/
public class EmailRule extends RegularExpressionRule {

	public static final String EMAIL_PATTERN = "^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$";
	 
	public static final String EMAIL_DISPLAY_FORMAT = "***@***";

	/**
    Creates an EmailRule object.
  */
  public EmailRule() {
    super(EMAIL_PATTERN, EMAIL_DISPLAY_FORMAT);
  }

}
